#!/bin/sh
# Regenerate src/registry.h from data/<tag>/.
#
#   tools/make_registry.sh v1 > src/registry.h
#
# ONE tag for the whole annotation set. Files live in the git repo at
# <base>/raw/<tag>/<file>, so a tag is a coherent snapshot of every platform.
#
# The registry pins the digest of SHA256SUMS, not of each file. That file
# carries the per-file digests, so:
#   - bumping the pinned tag is a one-line change here, not four digests;
#   - fetch can verify and de-duplicate ANY tag, not only the pinned one, by
#     reading that tag's SHA256SUMS;
#   - the pinned digest is still a hard trust anchor for the default tag.
#
# Two properties make one-tag-for-everything cheap: git is content-addressed, so
# an unchanged file costs zero bytes in a new tag; and fetch hardlinks from an
# existing tag when a digest matches, so an unchanged file is never downloaded
# twice.
set -eu

here=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
root=$(dirname "$here")
tag=${1:-v1}
base=${SESAME_BASE_URL:-https://github.com/zhou-lab/InfiniumAnnotation/raw}
src=${SESAME_INDEX_SRC:-$root/data/$tag}

# Bead count (IDAT nSNPsRead) -> platform. Only counts verified end-to-end are
# listed; anything else must be named with --platform rather than guessed at.
beads_for() {
    case $1 in
        HM450)  echo 622399  ;;
        EPIC)   echo 1051815 ;;
        EPICv2) echo 1105209 ;;
        MSA)    echo 384497  ;;
        *)      echo 0       ;;
    esac
}

sums_sha=$(shasum -a 256 "$src/SHA256SUMS" | cut -d' ' -f1)

cat <<EOF
/* registry.h -- GENERATED by tools/make_registry.sh. Do not edit.
 *
 * The pinned annotation tag, and the digest of that tag's SHA256SUMS -- the
 * trust anchor. Per-file digests come from SHA256SUMS itself, so verification
 * and de-duplication work for any tag, while the default tag stays anchored to
 * something compiled in.
 */
#ifndef SESAME_REGISTRY_H
#define SESAME_REGISTRY_H

#define SESAME_BASE_URL     "$base"
#define SESAME_DEFAULT_TAG  "$tag"
#define SESAME_SUMS_FILE    "SHA256SUMS"
#define SESAME_SUMS_SHA256  "$sums_sha"

typedef struct {
    const char *platform;
    int32_t     beads;     /* IDAT nSNPsRead; 0 = no auto-detect */
    const char *file;
} sesame_reg_t;

static const sesame_reg_t SESAME_REGISTRY[] = {
EOF

for f in "$src"/*.ordering.tsv.gz; do
    [ -e "$f" ] || continue
    b=$(basename "$f")
    plat=${b%%.ordering.tsv.gz}
    printf '    { "%s", %s, "%s" },\n' "$plat" "$(beads_for "$plat")" "$b"
done

cat <<'EOF'
    { NULL, 0, NULL }
};

#endif /* SESAME_REGISTRY_H */
EOF
