#!/bin/sh
# Regenerate src/registry.h from the ordering tables in data/.
#
#   tools/make_registry.sh [version] > src/registry.h
#
# The digests are computed from the actual files, so the registry and the
# artifacts cannot drift. Re-run this whenever an index is rebuilt, and publish
# the matching files to the release named by $version.
set -eu

here=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
root=$(dirname "$here")
ver=${1:-v1}
base=${SESAMEC_BASE_URL:-https://github.com/zhou-lab/sesamec/releases/download/index-$ver}

# Bead count (IDAT nSNPsRead) -> platform. Only counts we have actually
# verified end-to-end are listed; anything else must be named with --platform
# rather than guessed at.
beads_for() {
    case $1 in
        HM450)  echo 622399  ;;
        EPIC)   echo 1051815 ;;
        EPICv2) echo 1105209 ;;
        MSA)    echo 384497  ;;
        *)      echo 0       ;;
    esac
}

cat <<EOF
/* registry.h -- GENERATED by tools/make_registry.sh. Do not edit.
 *
 * Pinned index artifacts: filename, sha256 and size per platform, plus the
 * IDAT bead count used to auto-detect the platform.
 *
 * Digests are computed from the real files, so the registry cannot drift from
 * the artifacts. A silently-changed index would silently change betas, so the
 * digest is always verified after download.
 */
#ifndef SESAME_REGISTRY_H
#define SESAME_REGISTRY_H

#define SESAME_INDEX_VERSION  "$ver"
#define SESAME_INDEX_BASE_URL "$base"

typedef struct {
    const char *platform;
    int32_t     beads;     /* IDAT nSNPsRead; 0 = no auto-detect */
    const char *file;
    const char *sha256;
    long long   bytes;
} sesame_reg_t;

static const sesame_reg_t SESAME_REGISTRY[] = {
EOF

for f in "$root"/data/*.ordering.tsv.gz; do
    [ -e "$f" ] || continue
    b=$(basename "$f")
    plat=${b%%.ordering.tsv.gz}
    sum=$(shasum -a 256 "$f" | cut -d' ' -f1)
    size=$(wc -c < "$f" | tr -d ' ')
    printf '    { "%s", %s, "%s",\n      "%s", %s },\n' \
        "$plat" "$(beads_for "$plat")" "$b" "$sum" "$size"
done

cat <<'EOF'
    { NULL, 0, NULL, NULL, 0 }
};

#endif /* SESAME_REGISTRY_H */
EOF
